from math import sin, cos, tan, radians


# Design программы
print("""""""""""""""""""""""")
print("     )---------------------------------------------(")
print("        Калькулятор тригонометрического выражения ")
print("     )---------------------------------------------(")
print("""""""""""""""""""""""")




# Считываем угол в градусах
x = float(input("Введите градус угла: "))


# Переводим градусы в радианы с помощью функции radians()
angle_in_radians = radians(x)


# Вычисляем значение тригонометрического выражения
result = sin(angle_in_radians) + cos(angle_in_radians) + (tan(angle_in_radians) ** 2)


# Выводим результат
print("""""""""""""""""""""""")
print("Точное значение в Радианах: ", result)



