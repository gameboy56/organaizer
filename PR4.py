# Считываем количество школьников (n) и количество мандаринов (k)
n = int(input("Введите количество школьников: "))  # количество школьников
k = int(input("Введите количество мандаринов: "))  # количество мандаринов


# Вычисляем, сколько целых мандаринов достанется каждому школьнику
# Используем целочисленное деление (//) — оно возвращает целую часть от деления
mandarins_per_stud = k // n


# Вычисляем, сколько мандаринов останется в корзине
# Используем оператор остатка от деления (%) — он возвращает остаток
remainder = k % n


# Выводим результаты, каждый на отдельной строке
print("""""""""""""""""""""""")
print("|-------------------------------------------------|")
print("| Количество мандаринов на студента: ",mandarins_per_stud, "          |")
print("|-------------------------------------------------|")
print("| Количество оставшихся мандаринов в корзине: ",remainder, " |")
print("|-------------------------------------------------|")


