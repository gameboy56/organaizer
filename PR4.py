# Получаем координаты точек с ввода пользователя
x1 = float(input("Введите Первую координату: "))
y1 = float(input("Введите Вторую координату: "))
x2 = float(input("Введите Третью координату: "))
y2 = float(input("Введите Четвёртую координату: "))


# Вычисляем разности координат
dx = x2 - x1
dy = y2 - y1


# Вычисляем квадраты разностей
dx_squared = dx ** 2
dy_squared = dy ** 2


# Суммируем квадраты разностей
sum_of_squares = dx_squared + dy_squared


# Извлекаем квадратный корень из суммы — это и есть евклидово расстояние
distance = sum_of_squares ** 0.5


# Выводим результат
print("""""""""""""""""""""""")
print("Ответ: ", distance)

