print("""""""""""""""""""""""")
print("     )----------------------------------------(")
print("                Корректный IP‑адрес  ")
print("     )----------------------------------------(")
print("""""""""""""""""""""""")

ip_string = input("Введите IP-адрес: ")
octets = ip_string.split('.')
valid = True
if len(octets) != 4:
   valid = False
else:
   for octet in octets:
       if not octet.isdigit():  # проверка, что строка состоит из цифр
           valid = False
           break
       num = int(octet)
       if num < 0 or num > 255:
           valid = False
           break
print("ДА" if valid else "НЕТ")


