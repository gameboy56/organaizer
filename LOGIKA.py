# ------------------------- TASK 1 -------------------------

import math

HOURS_PER_DAY = 24  # Количество часов в сутках
PAST_PART_DAY = 0.2  # Часть времени, что прошла относительно того времени, что осталось (1/5 = 0.2)

# Вычисляем прошедшее время с начала суток
# Формула: прошедшее время = общее время / (коэффициент + 1)
time_passed = HOURS_PER_DAY / (PAST_PART_DAY + 1)

# Округляем до целого числа часов (если нужно)
time_passed = math.floor(time_passed)

# Вычисляем текущее время (сколько часов прошло с начала суток)
current_time = time_passed

print(f"Сейчас {current_time} часа")


