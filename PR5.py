import math

# Название программы
print("----------------------------------------")
print("     Калькулятор площади треугольника    ")
print("----------------------------------------")
print("""""""""""""""""""""""")


# Функция для вычисления расстояния между точками
def calculate_distance(x1, y1, x2, y2):
    # Считаем расстояние по формуле
    return math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)


# Функция для вычисления площади треугольника
def calculate_triangle_area(a, b, c):
    # Проверяем, существует ли треугольник
    if a + b <= c or a + c <= b or b + c <= a:
        return "Треугольник с такими сторонами не существует!"

    # Вычисляем полупериметр
    p = (a + b + c) / 2
    # Считаем площадь по формуле Герона
    return math.sqrt(p * (p - a) * (p - b) * (p - c))


# Основная программа
print("Привет! Давай посчитаем площадь треугольника.")
print("Нужно ввести координаты трёх точек.")

# Вводим первую точку
print("\n---Первая точка---")
x1 = float(input("Введи координату X: "))
y1 = float(input("Введи координату Y: "))

# Вводим вторую точку
print("\n---Вторая точка---")
x2 = float(input("Введи координату X: "))
y2 = float(input("Введи координату Y: "))

# Вводим третью точку
print("\n---Третья точка---")
x3 = float(input("Введи координату X: "))
y3 = float(input("Введи координату Y: "))

# Считаем длины сторон
side1 = calculate_distance(x1, y1, x2, y2)
side2 = calculate_distance(x2, y2, x3, y3)
side3 = calculate_distance(x3, y3, x1, y1)

# Вычисляем площадь
area = calculate_triangle_area(side1, side2, side3)

