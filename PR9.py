# Design программы
print("""""""""""""""""""""""")
print("     )----------------------------------------(")
print("                     Все вместе                ")
print("     )----------------------------------------(")
print("""""""""""""""""""""""")


number = input("Введите число: ")
count_3 = 0  # счетчик троек
last_digit = number[-1]  # последняя цифра
count_last = 0  # счетчик последней цифры
even_count = 0  # счетчик четных
sum_over_5 = 0  # сумма больше 5
product_over_7 = 1  # произведение больше 7
count_0_5 = 0  # счетчик 0 и 5


for digit in number:
   digit = int(digit)  # преобразование в число

   if digit == 3:
       count_3 += 1

   if digit == int(last_digit):
       count_last += 1

   if digit % 2 == 0:
       even_count += 1

   if digit > 5:
       sum_over_5 += digit

   if digit > 7:
       product_over_7 *= digit

   if digit == 0 or digit == 5:
       count_0_5 += 1


# отдельный цикл если не было цифр больше 7
if product_over_7 == 1 and sum_over_5 == 0:
   product_over_7 = 1
elif product_over_7 == 1:
   product_over_7 = 0


print(f"Количество троек: {count_3}")
print(f"Последняя цифра встречается: {count_last} раз")
print(f"Четных цифр: {even_count}")
print(f"Сумма цифр > 5: {sum_over_5}")
print(f"Произведение цифр > 7: {product_over_7}")
print(f"Количество 0 и 5: {count_0_5}")
