# номиналы чеканок
coins = [25, 10, 5, 1]
print("!!! Внимание: существуют номиналы только в [1,5,10,25] ")
print("""""""""""""""""""""""")
price = int(input("Введите цену: "))
total_coins = 0  # счетчик монет


# перебор номиналов от большего к меньшему
for coin in coins:
  while price >= coin:  # пока можем взять чеканную монету
      price -= coin  # отжимаем номинал
      total_coins += 1  # увеличиваем счетчик


print("""""""""""""""""""""""")
print(f"Минимальное количество чеканных монет: {total_coins}")



